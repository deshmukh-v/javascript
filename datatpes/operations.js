//****************PREFIX & POSTFIX *******************************

/* In the prefix version (++i), the value of i is incremented first, and the value of the expression is the new value of i.
In the postfix version (i++), the value of i is incremented, but the value of the expression is the original value of i.
*/
//PREFIC//
let a=10;
let b= ++a;          //increase by one
console.log(b);

//POSTFIX//

let c=20;
let d=c++;          //not incresed
console.log(d);